/* ========================================================================
 * Test 6: SMP g2 (BT Core Spec Vol 3, Part H, D.5)
 *
 * g2(U, V, X, Y) = AES-CMAC_X(U || V || Y) mod 10^6
 * Numeric comparison value for LE Secure Connections.
 * ======================================================================== */

static void
_TestG2()
{
	printf("\n--- SMP g2 (BT Core Spec D.5) ---\n");

	const uint8 U[32] = {
		0x20, 0xb0, 0x03, 0xd2, 0xf2, 0x97, 0xbe, 0x2c,
		0x5e, 0x2c, 0x83, 0xa7, 0xe9, 0xf9, 0xa5, 0xb9,
		0xef, 0xf4, 0x91, 0x11, 0xac, 0xf4, 0xfd, 0xdb,
		0xcc, 0x03, 0x01, 0x48, 0x0e, 0x35, 0x9d, 0xe6
	};
	const uint8 V[32] = {
		0x55, 0x18, 0x8b, 0x3d, 0x32, 0xf6, 0xbb, 0x9a,
		0x90, 0x0a, 0xfc, 0xfb, 0xee, 0xd4, 0xe7, 0x2a,
		0x59, 0xcb, 0x9a, 0xc2, 0xf1, 0x9d, 0x7c, 0xfb,
		0x6b, 0x4f, 0xdd, 0x49, 0xf4, 0x7f, 0xc5, 0xfd
	};
	const uint8 X[16] = {
		0xd5, 0xcb, 0x84, 0x54, 0xd1, 0x77, 0x73, 0x3e,
		0xff, 0xff, 0xb2, 0xec, 0x71, 0x2b, 0xae, 0xab
	};
	const uint8 Y[16] = {
		0xa6, 0xe8, 0xe7, 0xcc, 0x25, 0xa7, 0x5f, 0x6e,
		0x21, 0x65, 0x83, 0xf7, 0xff, 0x3d, 0xc4, 0xcf
	};

	uint32 passkey = 0;
	smp_g2(U, V, X, Y, &passkey);

	/* g2 returns AES-CMAC result mod 10^6, we verify the full CMAC first */
	uint8 m[80];
	memcpy(m, U, 32);
	memcpy(m + 32, V, 32);
	memcpy(m + 64, Y, 16);
	uint8 cmacResult[16];
	smp_aes_cmac(X, m, 80, cmacResult);

	printf("  g2 CMAC result: ");
	for (int i = 0; i < 16; i++)
		printf("%02x", cmacResult[i]);
	printf("\n");

	/* Extract last 4 bytes as big-endian uint32, mod 10^6 */
	uint32 val = ((uint32)cmacResult[12] << 24)
		| ((uint32)cmacResult[13] << 16)
		| ((uint32)cmacResult[14] << 8)
		| cmacResult[15];
	uint32 expectedPasskey = val % 1000000;

	if (passkey == expectedPasskey) {
		printf("[PASS] g2 passkey = %u (from 0x%08x)\n", passkey, val);
		sTestsPassed++;
	} else {
		printf("[FAIL] g2 passkey: expected %u, got %u\n",
			expectedPasskey, passkey);
		sTestsFailed++;
	}
}


